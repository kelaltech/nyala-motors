export function nameEachCat(category: string) {
  switch (category) {
    case 'CROSSOVER':
      return 'CROSSOVER'
    case 'SPORT_UTILITY':
      return 'SPORT UTILITY'
    case 'COMMERCIAL':
      return 'COMMERCIAL'
    case 'CRONER':
      return 'CRONER'
    case 'QUESTER':
      return 'QUESTER'
    case 'PASSENGER':
      return 'PASSENGER'
  }
}
